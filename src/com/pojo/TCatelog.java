package com.pojo;
public class TCatelog
{
 
	public TCatelog(){
  
	}
	 
	private Long id;
        
         
	private String name;
        
         
	private Long fid;
        
         
	private String del;
        
        
         
	public Long getId(){
		return this.id;
	}
 
	public void setId(Long id){
		this.id = id;
	}
		
         
	public String getName(){
		return this.name;
	}
 
	public void setName(String name){
		this.name = name;
	}
		
         
	public Long getFid(){
		return this.fid;
	}
 
	public void setFid(Long fid){
		this.fid = fid;
	}
		
         
	public String getDel(){
		return this.del;
	}
 
	public void setDel(String del){
		this.del = del;
	}
		
            
}
